    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        Type type = (Type) organizationJComboBox.getSelectedItem();
        if (type.getValue().equals(Type.Welfare.getValue())) {
            JOptionPane.showMessageDialog(null, "The selected organization already exists ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (type.getValue().equals(Type.DHSS.getValue())) {
            JOptionPane.showMessageDialog(null, "The selected organization already exists ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (type.getValue().equals(Type.Doctor.getValue())) {
            JOptionPane.showMessageDialog(null, "The selected organization already exists ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (type.getValue().equals(Type.Lab.getValue())) {
            JOptionPane.showMessageDialog(null, "The selected organization already exists ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (type.getValue().equals(Type.Pharmacy.getValue())) {
            JOptionPane.showMessageDialog(null, "The selected organization already exists ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            directory.createOrganization(type);
            populateTable();
        }
    }//GEN-LAST:event_addJButtonActionPerformed

