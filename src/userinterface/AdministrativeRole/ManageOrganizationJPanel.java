    private void populateCombo() {
        organizationJComboBox.removeAllItems();
        for (Organization.Type type : Organization.Type.values()) {
            if (enterprise.getEnterpriseType().getValue().equals(Enterprise.EnterpriseType.Hospital.getValue())) {
                if (type.equals(Organization.Type.Lab)) {
                    organizationJComboBox.addItem(type);
                } else if (type.equals(Organization.Type.Doctor)) {
                    organizationJComboBox.addItem(type);
                } else if (type.equals(Organization.Type.Pharmacy)) {
                    organizationJComboBox.addItem(type);
                }
            } else if (enterprise.getEnterpriseType().getValue().equals(Enterprise.EnterpriseType.NGO.getValue())) {
                if (type.equals(Organization.Type.Welfare)) {
                    organizationJComboBox.addItem(type);
                } else if (type.equals(Organization.Type.DHSS)) {
                    organizationJComboBox.addItem(type);
                }
            } else {
                if (!type.getValue().equals(Organization.Type.Admin.getValue()) && !type.getValue().equals(Organization.Type.Doctor.getValue())
                        && !type.getValue().equals(Organization.Type.Pharmacy.getValue()) && !type.getValue().equals(Organization.Type.Welfare.getValue())) {
                    organizationJComboBox.addItem(type);
                }
            }
        }
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();

        model.setRowCount(0);

        for (Organization organization : directory.getOrganizationList()) {
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();

            model.addRow(row);
        }
    }

