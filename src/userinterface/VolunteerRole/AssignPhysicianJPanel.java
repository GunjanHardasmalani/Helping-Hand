    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        String physician = txtPhysicianComboBox.getSelectedItem().toString();
        patientApprovalWorkRequest.setAssignedPhysician(physician);
        JOptionPane.showMessageDialog(null, "Physician " + physician + " assigned to Patient "+ txtPatientNameField.getText());
        try {
            logs.generateLogs(AssignPhysicianJPanel.class.getName(), physician +" assigned to" +patientApprovalWorkRequest.getPatientName());
        } catch (IOException ex) {
            Logger.getLogger(AssignPhysicianJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAssignActionPerformed

