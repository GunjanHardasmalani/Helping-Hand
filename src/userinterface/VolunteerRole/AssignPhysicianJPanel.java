    public void populatePhysicianComboBox() {
        for (UserAccount a : welfareOrganization.getUserAccountDirectory().getUserAccountList()) {
            if (a.getRole().toString().equals("Business.Role.PhysicianRole")) {
                txtPhysicianComboBox.addItem(a.getEmployee().getName());
            }
        }
    }

