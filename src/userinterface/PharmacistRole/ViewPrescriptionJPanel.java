    public void populateFields() {
        txtName.setText(patientPrescriptionWorkRequest.getPatientName());
        txtAge.setText(String.valueOf(patientPrescriptionWorkRequest.getAge()));
        txtDate.setText(patientPrescriptionWorkRequest.getDatePrescribed());
        txtSignature.setText(patientPrescriptionWorkRequest.getDocName());

        DefaultTableModel dtm = (DefaultTableModel) tblMedicines.getModel();
        dtm.setRowCount(0);
        for (Entry<String, String> e : patientPrescriptionWorkRequest.getMedicineList().entrySet()) {
            Object[] row = new Object[2];
            row[0] = e.getKey();
            row[1] = e.getValue();
            dtm.addRow(row);
        }
    }

