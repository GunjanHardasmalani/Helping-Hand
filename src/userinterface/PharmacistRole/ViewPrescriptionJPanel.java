    private void btnApproveRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveRequestActionPerformed
        // TODO add your handling code here:
        if(patientPrescriptionWorkRequest.getStatus().equals("Completed")){
            JOptionPane.showMessageDialog(null, "Request already approved");
        }else{
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to approve the patient's prescription?", "Warning", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            patientPrescriptionWorkRequest.setStatus("Completed");
        }
        }
    }//GEN-LAST:event_btnApproveRequestActionPerformed


